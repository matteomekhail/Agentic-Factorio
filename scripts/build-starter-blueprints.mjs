#!/usr/bin/env node
// Regenerates the mod's starter blueprint data from BlueprintBooks/*.txt.
//
//   BlueprintBooks/<order>-<name>.txt   (one Factorio export string per file)
//        │  npm run blueprints:build
//        ▼
//   mod/agentic-companion/scripts/starter_blueprints.lua        (index)
//   mod/agentic-companion/scripts/starter_blueprints/book_N.lua (one per file)
//
// Each string is validated (base64 + zlib + JSON) and its in-game label is
// extracted so the mod can find/replace the books it issued. The index carries
// a content-hash `version`: any change re-issues the books to existing saves.
import { createHash } from "node:crypto";
import { inflateSync } from "node:zlib";
import { mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repo = dirname(dirname(fileURLToPath(import.meta.url)));
const srcDir = join(repo, "BlueprintBooks");
const outIndex = join(repo, "mod/agentic-companion/scripts/starter_blueprints.lua");
const outDir = join(repo, "mod/agentic-companion/scripts/starter_blueprints");

/** Decode a Factorio export string into its JSON payload (throws if invalid). */
function decode(exportString, file) {
  if (!/^0[A-Za-z0-9+/=]+$/.test(exportString)) {
    throw new Error(`${file}: not a Factorio export string (must be '0' + base64)`);
  }
  const json = JSON.parse(inflateSync(Buffer.from(exportString.slice(1), "base64")).toString("utf8"));
  const root =
    json.blueprint_book ?? json.blueprint ?? json.upgrade_planner ?? json.deconstruction_planner;
  if (!root) throw new Error(`${file}: decoded JSON has no blueprint/blueprint_book root`);
  const countPrints = (node) =>
    node.blueprint_book
      ? (node.blueprint_book.blueprints ?? []).reduce((n, k) => n + countPrints(k), 0)
      : 1;
  return {
    label: typeof root.label === "string" ? root.label : null,
    isBook: Boolean(json.blueprint_book),
    prints: json.blueprint_book ? countPrints(json) : 1,
  };
}

/** Lua literal: long brackets when safe (export strings are base64, so always),
 *  else a double-quoted string with escapes (labels can contain anything). */
function luaString(s) {
  if (!s.includes("]]") && !s.includes("\n") && !s.includes("\r")) return `[[${s}]]`;
  return `"${s.replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\r/g, "\\r").replace(/\n/g, "\\n")}"`;
}

const files = readdirSync(srcDir)
  .filter((f) => f.endsWith(".txt"))
  .sort();
if (files.length === 0) {
  console.error(`No .txt export strings found in ${srcDir}`);
  process.exit(1);
}

const books = files.map((file) => {
  const raw = readFileSync(join(srcDir, file), "utf8").trim();
  const meta = decode(raw, file);
  const label = meta.label ?? file.replace(/\.txt$/, "");
  console.log(
    `  ${file}: ${meta.isBook ? "book" : "single print"} "${label}" (${meta.prints} print${meta.prints === 1 ? "" : "s"})`,
  );
  return { file, label, string: raw };
});

const version = "sha1:" + createHash("sha1").update(books.map((b) => b.string).join("\n")).digest("hex").slice(0, 12);

const header = (source) =>
  `-- AUTO-GENERATED by scripts/build-starter-blueprints.mjs — DO NOT EDIT.\n` +
  `-- Source: BlueprintBooks/${source}. Regenerate with: npm run blueprints:build\n`;

rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });
books.forEach((b, i) => {
  const n = i + 1;
  writeFileSync(
    join(outDir, `book_${n}.lua`),
    header(b.file) +
      `return {\n` +
      `  label = ${luaString(b.label)},\n` +
      `  source = ${luaString(b.file)},\n` +
      `  string = ${luaString(b.string)},\n` +
      `}\n`,
  );
});

writeFileSync(
  outIndex,
  header("*.txt") +
    `return {\n` +
    `  version = "${version}",\n` +
    `  books = {\n` +
    books.map((_, i) => `    require("scripts.starter_blueprints.book_${i + 1}"),`).join("\n") +
    `\n  },\n` +
    `}\n`,
);

console.log(`Wrote ${books.length} book(s), version ${version}`);
console.log(`  ${outIndex}`);
console.log(`  ${outDir}/book_1..${books.length}.lua`);
